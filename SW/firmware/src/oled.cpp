/**
 * @brief function for displaying the author info Page
 */
void deviceInfo()
{
    // Open the JSON file
    FILE *file = fopen("/spiffs/device.json", "r");
    if (!file)
    {
        Serial.println("Failed to open deviceinfo.json");
        return;
    }

    // Read the file into a buffer
    char buffer[256];
    size_t bytesRead = fread(buffer, 1, sizeof(buffer) - 1, file);
    buffer[bytesRead] = '\0'; // Null-terminate the buffer
    fclose(file);

    // Parse the JSON data
    StaticJsonDocument<512> doc;
    DeserializationError error = deserializeJson(doc, buffer);
    if (error)
    {
        Serial.print("Failed to parse JSON: ");
        Serial.println(error.c_str());
        return;
    }

    // Extract data from JSON
    JsonObject deviceInfo = doc["deviceInfo"];
    const char *deviceName = deviceInfo["device Name"];
    const char *fwVersion = deviceInfo["FW Version"];
    const char *hwVersion = deviceInfo["HW Version"];
    const char *serialNumber = deviceInfo["SN"];

    // Display the data on the OLED
    display.clearDisplay();
    display.setCursor(0, 0);
    display.println("Device Info:");
    display.println((String) "");
    display.println((String) "Project: " + deviceName);
    display.println((String) "FW Ver:  " + fwVersion);
    display.println((String) "HW Ver:  " + hwVersion);
    display.println((String) "SN:      " + serialNumber);
    display.display();
}
